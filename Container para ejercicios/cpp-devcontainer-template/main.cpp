#include <iostream>
using namespace std;

// 'main' es el punto de entrada del programa
// siempre debe definirse, siempre devuelve int
int main() {
    // cout: salida por pantalla
    // el operador << "envía datos" a cout (pantalla)
    // endl representa a "enter" 
    cout << "Hola, mundo!" << endl;

    // La función main siempre debe devolver 0,
    // puede omitirse y se devolverla 0 por defecto.
    return 0;
}